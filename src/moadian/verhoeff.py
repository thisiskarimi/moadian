def checkSum(number):
    c = 0
    len_ = len(number)
    for i in range(len_):
        c = MULTIPLICATION_TABLE[c][PERMUTATION_TABLE[(
            (i + 1) % 8)][int(number[len_ - i - 1]) - 0]]
    return INVERSE_TABLE[c]


def validate(number):
    c = 0
    len = len(number)
    for i in range(len):
        c = MULTIPLICATION_TABLE[c][PERMUTATION_TABLE[(
            i % 8)][int(number[len - i - 1]) - 0]]
    return c == 0
